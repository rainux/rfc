/// Return the 8-byte glyph for a given character.
pub fn glyph(ch: char) -> &'static [u8] {
    let idx = ch as usize;
    if idx < GLYPH_COUNT {
        &FONT_DATA[idx * 8..(idx + 1) * 8]
    } else {
        &FONT_DATA[0..8] // NUL = blank
    }
}

/// Draw a text string into an RGBA frame buffer.
///
/// * `buf` — 256 x 240 x 4 RGBA pixel buffer
/// * `text` — ASCII string to render
/// * `x`, `y` — top-left position in pixels
/// * `color` — RGB colour for the text
pub fn draw_text(buf: &mut [u8], text: &str, x: usize, y: usize, color: [u8; 3]) {
    const STRIDE: usize = 256;
    const HEIGHT: usize = 240;

    for (ci, ch) in text.chars().enumerate() {
        let g = glyph(ch);
        for row in 0..8 {
            let byte = g[row];
            for col in 0..8 {
                if byte & (0x80 >> col) != 0 {
                    let px = x + ci * 8 + col;
                    let py = y + row;
                    if px < STRIDE && py < HEIGHT {
                        let off = (py * STRIDE + px) * 4;
                        buf[off] = color[0];
                        buf[off + 1] = color[1];
                        buf[off + 2] = color[2];
                        buf[off + 3] = 255;
                    }
                }
            }
        }
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn glyph_a_not_blank() {
        let g = glyph('A');
        assert!(
            g.iter().any(|&b| b != 0),
            "Glyph for 'A' should not be blank"
        );
    }

    #[test]
    fn glyph_unknown_is_blank() {
        let g = glyph('\u{FF}');
        assert!(g.iter().all(|&b| b == 0), "Unknown glyph should be blank");
    }

    #[test]
    fn draw_text_within_bounds() {
        let mut buf = vec![0u8; 256 * 240 * 4];
        draw_text(&mut buf, "AB", 0, 0, [255, 255, 255]);
        // At least some pixels should be non-zero
        assert!(buf.iter().any(|&b| b != 0));
    }
}
